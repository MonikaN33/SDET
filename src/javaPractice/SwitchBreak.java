package javaPractice;

public class SwitchBreak 
{

	public static void main(String[] args) 
	{
		char color = 'g';
		
		switch(color)
		{
		case 'r' : System.out.println("Red");
		break;
		case 'g' : System.out.println("Green");
		break;
		case 'b' : System.out.println("Blue");
		break;
		case 'p' : System.out.println("Pink");
		break;
		}

	}

}
